    private void findBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_findBtnActionPerformed
        // TODO add your handling code here:
        String s = JOptionPane.showInputDialog("Nhập tên người dùng cần tìm kiếm");
        if (!s.isEmpty()) {
            s = "%" + s + "%";
        }
        dataList = DataUserFunction.getUserList(s);
        showData();
    }//GEN-LAST:event_findBtnActionPerformed

    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        // TODO add your handling code here:
        if (currentPos == -1) {
            JOptionPane.showMessageDialog(rootPane, "Chưa chọn người dùng cần xóa, vui lòng kiểm tra lại");
            return;
        }
        int option = JOptionPane.showConfirmDialog(rootPane, "Bạn chắc chắn muốn xóa người dùng này không?");
        if (option == 0) {
            DataUserFunction.deleteUser(dataList.get(currentPos).getId());

            dataList.remove(currentPos);
            currentPos = -1;
            showData();
        }

        FullName.setText("");
        Dob.setText("");
        Address.setText("");
        PhoneNumber.setText("");
        Gender.setText("");

    }//GEN-LAST:event_deleteBtnActionPerformed

    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed
        // TODO add your handling code here:
        if (currentPos >= 0) {

            dataList.get(currentPos).setName(FullName.getText());
            dataList.get(currentPos).setDob(Dob.getText());
            dataList.get(currentPos).setAddress(Address.getText());
            try {
                dataList.get(currentPos).setPhone(PhoneNumber.getText());
            } catch (InValidPhoneException ex) {
                Logger.getLogger(UserForm.class.getName()).log(Level.SEVERE, null, ex);
            }
            dataList.get(currentPos).setGender(Gender.getText());
            DataUserFunction.updateUser(dataList.get(currentPos));

            currentPos = -1;
            JOptionPane.showMessageDialog(rootPane, "Cập nhật thành công thông tin!");
        } else {
            JOptionPane.showMessageDialog(rootPane, "Chưa chọn người dùng cần sửa, vui lòng kiểm tra lại !");
        }
        showData();

        FullName.setText("");
        Dob.setText("");
        Address.setText("");
        PhoneNumber.setText("");
        Gender.setText("");
    }//GEN-LAST:event_updateBtnActionPerformed

