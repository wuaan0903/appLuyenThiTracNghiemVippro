    private void showData() {
        tableModel.setRowCount(0);
        for (employee e : dataList) {
            tableModel.addRow(new Object[]{
                tableModel.getRowCount() + 1,
                e.getNameTK(),
                e.getPassword(),
                e.getName(),
                e.getDob(),
                e.getAddress(),
                e.getPhone(),
                e.getGender()
            });
        }
    }

